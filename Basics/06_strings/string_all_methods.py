#String Functions
text_string = "ram is a good good Boy 2323"

print(text_string.capitalize()) #Make the first letter of the word uppercase
print(text_string.count("good")) #Counts the given string
print(text_string.endswith("Boy")) #check that the given String ends with Boy or not
print(text_string.startswith("Ram")) #check that the given String Starts with Ram or not
print(text_string.find("good")) #return the index of the first occurence of the given Str
print(text_string.index("good")) #return the index of the first occurence of the given Str
print(text_string.isalnum()) #check the given string is alpha-numeric or not
print(text_string.isspace()) #if the str is empty and minimum there will be one character then return true else false
print(text_string.upper()) #Return a copy of the string converted to uppercase.
print(text_string.title()) #Return a version of the string where each word is titlecased
print(text_string.split()) #Return a list of the substrings in the string, using sep as the separator string.
print(text_string.isupper()) #Return True if the string is an uppercase string, False otherwise.
