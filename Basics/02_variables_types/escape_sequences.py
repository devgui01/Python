# "\t" is for tab space and "\n" is for new line
full_name = "Raj\tPal\tYadav is a good boy,\nhe love to read books."

# If we use double quote or single quote with "\" it act as same
class_message = "How are you ? \"hope you are fine\" "
roll_message = 'How are you ? \'hope you are fine\' '
print(full_name, class_message, roll_message)
